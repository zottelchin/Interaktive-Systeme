    public void inputRawData(String input){
        AnzahlSensordaten = convertHextoDec(input.substring(12, 17));
        input = input.substring(18).replace(" ", "");
        int[] tempArray = new int[AnzahlSensordaten];
        for (int i = 0; i < AnzahlSensordaten; i++){
            int temp = convertHextoDec(input.substring(i*4,4+i*4));
            temp = doCrazyShit(temp);
            tempArray[i] = temp;
        }

        int iterate = 0;
        for (int i = 0; i < 5; i++) {
            for (int j = 0; j < 10; j++){
                if (tempArray[iterate]>15){
                    Sensordata[j][i] = tempArray[iterate];
                }else {
                    j--;
                }
                iterate++;

            }
        }
        System.out.println(this.toString());

    }

    private int convertHextoDec(String AnzahlString){
        return Integer.parseInt(AnzahlString.replace(" ", ""), 16);
    }

    private int doCrazyShit(int a){
        int M_RANGE0_OFFSET = 0;
        int M_RANGE1_OFFSET = 1843;
        int M_RANGE2_OFFSET = 3502;
        int M_RANGE3_OFFSET = 5161;

        if (a > M_RANGE3_OFFSET)
            a = ((a - M_RANGE3_OFFSET) + 184) * 1000;
        else if (a > M_RANGE2_OFFSET)
            a = ((a - M_RANGE2_OFFSET) + 184) * 100;
        else if (a > M_RANGE1_OFFSET)
            a = ((a - M_RANGE1_OFFSET) + 184) * 10;
        else if (a > M_RANGE0_OFFSET)
            a = (a - M_RANGE0_OFFSET) * 1;

        return a;
    }

